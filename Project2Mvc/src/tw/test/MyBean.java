package tw.test;

public class MyBean {

	int id;
	String str;
	
	public MyBean() {

	}
	
	public MyBean(int id, String str) {
		this.id = id;
		this.str = str;
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getStr() {
		return str;
	}
	public void setStr(String str) {
		this.str = str;
	}
	
	
}
